package ua.kiev.prog02.ProgKievUaMarathon202003;

public class Mar_001145_L02P04_03_if_else {
    public static void main(String[] args) {
        int x = 10;
        System.out.println("x = " + x);
        if (x == 10)
            System.out.println("for x == 10: " + "yes");
        int y = 10;
        System.out.println("y = " + y);
        if (y == 11) {
            System.out.println("for y == 11: " + "yes");
        } else {
            System.out.println("for y == 11: " + "no");
        }
    }
}
